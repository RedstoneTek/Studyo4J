package com.tek.studyo4j.struct;

public class DayConfiguration {
	
	private String id;
	private int dayOfWeek;
	private String cycleDay;
	private String date;
	private String scheduleId;
	private String specialDayId;
	
	public String getId() {
		return id;
	}
	
	public int getDayOfWeek() {
		return dayOfWeek;
	}
	
	public String getCycleDay() {
		return cycleDay;
	}
	
	public String getDate() {
		return date;
	}
	
	public String getScheduleId() {
		return scheduleId;
	}
	
	public String getSpecialDayId() {
		return specialDayId;
	}
	
}
